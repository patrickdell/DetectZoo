"""Self-Synthesis GAN (9-GAN) – GANGen-Detection.

Reference:
    Tan et al., "GANGen-Detection: A Dataset generated by GANs for Generalizable deepfake
    Detection", 2024.

GitHub: https://github.com/chuangchuangtan/GANGen-Detection
Data hosted on Google Drive: https://drive.google.com/drive/folders/11E0Knf9J1qlv2UuTnJSOFUjIIi90czSj?usp=sharing
"""

from __future__ import annotations

from pathlib import Path
from typing import List, Optional, Sequence, Tuple

from detectzoo.datasets.base import BaseDataset, DatasetItem
from detectzoo.datasets._download import extract_tar_archive
from detectzoo.core.registry import register_dataset

_DEFAULT_GENERATORS: Tuple[str, ...] = (
    "AttGAN",
    "BEGAN",
    "CramerGAN",
    "InfoMaxGAN",
    "MMDGAN",
    "RelGAN",
    "S3GAN",
    "SNGAN",
    "STGAN",
)

_IMAGE_EXTS = frozenset({".png", ".jpg", ".jpeg", ".webp", ".bmp", ".tif", ".tiff"})

_GDRIVE_GANGEN_FOLDER = (
    "https://drive.google.com/drive/folders/11E0Knf9J1qlv2UuTnJSOFUjIIi90czSj?usp=sharing"
)


def _gan_layout_ok(base: Path, generators: Sequence[str]) -> bool:
    return all(
        (base / g / "0_real").is_dir() and (base / g / "1_fake").is_dir() for g in generators
    )


@register_dataset("self_synthesis")
class SelfSynthesisDataset(BaseDataset):
    """GANGen-Detection: nine GAN families with ``0_real`` / ``1_fake`` per generator.


    Parameters
    ----------
    root : str or Path, optional
        Directory that will contain ``Generalization_Test/GANGen-Detection/``. Default:
        ``.detectzoo_data/self_synthesis/``.
    partitions : sequence of str, optional
        Subset of the nine GAN partitions; *None* loads all.
    cache_dir : str or Path, optional
        Root cache directory (default ``.detectzoo_data``).
    """

    name: str = "self_synthesis"
    modality: str = "image"

    def __init__(
        self,
        root: str | Path | None = None,
        partitions: Optional[Sequence[str]] = None,
        cache_dir: str | Path | None = None,
    ) -> None:
        from detectzoo.datasets._download import get_cache_dir

        super().__init__()
        self.root = Path(root) if root is not None else get_cache_dir("self_synthesis", cache_dir)
        self._gangen_root = self.root / "Generalization_Test" / "GANGen-Detection"
        if partitions is None:
            self.partitions = list(_DEFAULT_GENERATORS)
        else:
            self.partitions = list(partitions)
            invalid = [p for p in self.partitions if p not in _DEFAULT_GENERATORS]
            if invalid:
                raise ValueError(
                    f"Unknown SelfSynthesis partition(s) {invalid!r}. "
                    f"Valid: {list(_DEFAULT_GENERATORS)}"
                )
        self.cache_dir = cache_dir

    def _ensure_download(self) -> None:
        gens = self.partitions
        dest_parent = self.root / "Generalization_Test"
        dest_parent.mkdir(parents=True, exist_ok=True)

        if self._finalize_gangen_layout(dest_parent, gens):
            return

        try:
            import gdown
        except Exception as e:
            raise ModuleNotFoundError(
                "Missing dependency `gdown`. Install with `pip install gdown`."
            ) from e

        gdown.download_folder(_GDRIVE_GANGEN_FOLDER, output=str(dest_parent), quiet=False, use_cookies=False)

        if not self._finalize_gangen_layout(dest_parent, gens):
            raise RuntimeError(
                "GANGen-Detection download did not produce the expected layout "
                f"(see `{dest_parent}`)."
            )

    def _relocate_generators_into_gangen(self, src_base: Path, gens: Sequence[str]) -> None:
        self._gangen_root.mkdir(parents=True, exist_ok=True)
        for gen in gens:
            s, d = src_base / gen, self._gangen_root / gen
            if d.exists():
                continue
            if s.exists():
                s.rename(d)

    def _finalize_gangen_layout(self, dest_parent: Path, gens: Sequence[str]) -> bool:
        """Return True once ``_gangen_root`` has the expected per-generator layout."""
        if self._gangen_root.exists() and _gan_layout_ok(self._gangen_root, gens):
            return True
        if _gan_layout_ok(dest_parent, gens):
            self._relocate_generators_into_gangen(dest_parent, gens)
            return self._gangen_root.exists() and _gan_layout_ok(self._gangen_root, gens)

        if any((dest_parent / f"{g}.tar.gz").exists() for g in gens):
            for g in gens:
                p = dest_parent / f"{g}.tar.gz"
                if p.exists():
                    extract_tar_archive(p, dest_parent)
            if _gan_layout_ok(dest_parent, gens):
                self._relocate_generators_into_gangen(dest_parent, gens)
                return self._gangen_root.exists() and _gan_layout_ok(self._gangen_root, gens)

        want = set(gens)
        for att in dest_parent.rglob("AttGAN"):
            cand = att.parent
            if not cand.is_dir():
                continue
            if not want.issubset({p.name for p in cand.iterdir() if p.is_dir()}):
                continue
            if not _gan_layout_ok(cand, gens):
                continue
            if cand != self._gangen_root:
                self._gangen_root.parent.mkdir(parents=True, exist_ok=True)
                if not self._gangen_root.exists():
                    cand.rename(self._gangen_root)
            return self._gangen_root.exists() and _gan_layout_ok(self._gangen_root, gens)

        return False

    def _load_all(self) -> List[DatasetItem]:
        self._ensure_download()

        gens = self.partitions
        items: List[DatasetItem] = []

        for gen in gens:
            real_dir = self._gangen_root / gen / "0_real"
            fake_dir = self._gangen_root / gen / "1_fake"
            if not real_dir.is_dir() or not fake_dir.is_dir():
                raise FileNotFoundError(f"Missing {real_dir} or {fake_dir}")

            for label, directory, source in (
                (0, real_dir, "real"),
                (1, fake_dir, "fake"),
            ):
                for path in sorted(directory.rglob("*")):
                    if path.is_file() and path.suffix.lower() in _IMAGE_EXTS:
                        items.append(
                            DatasetItem(
                                data=str(path),
                                label=label,
                                metadata={
                                    "split": "test",
                                    "partition": gen,
                                    "generator": gen,
                                    "source": source,
                                },
                            )
                        )

        return items
